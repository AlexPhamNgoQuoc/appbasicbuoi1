# Nhập độ dài và chiều rộng từ người dùng
d = float(input("Nhập độ dài của hình chữ nhật: "))
r = float(input("Nhập chiều rộng của hình chữ nhật: "))

# Tính chu vi và diện tích
chu_vi = 2 * (d + r)
dien_tich = d * r

# In ra kết quả
print("Chu vi của hình chữ nhật là:", chu_vi)
print("Diện tích của hình chữ nhật là:", dien_tich)
